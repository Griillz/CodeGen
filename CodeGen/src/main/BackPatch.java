package main;

public class BackPatch {
	int start = 0;
	int end = 0;
	
	public BackPatch() {
		
	}
	
	public BackPatch(int i) {
		this.start = i;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}
	
	
}
